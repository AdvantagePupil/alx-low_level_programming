#include "cal.h"

/**
 * op_add - Entry point
 *
 * description: function that gives you the sum of two ints
 *
 * @a: int
 *
 * @b: int
 *
 * Return: sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Entry point
 *
 * description: function that substracs two ints
 *
 * @a: int
 *
 * @b: int
 *
 * Return: substraction result
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Entry point
 *
 * description: function that multiplies two ints
 *
 * @a: int
 *
 * @b: int
 *
 * Return: multiplication result
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Entry point
 *
 * description: function that divides two ints
 *
 * @a: int
 *
 * @b: int
 *
 * Return: division result
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Entry point
 *
 * description: function that modulo two ints
 *
 * @a: int
 *
 * @b: int
 *
 * Return: mod result
 */

int op_mod(int a, int b)
{
	return (a % b);
}
